public class TestClass
{
	public static String isPositive(int arg)
	{
		if (arg >= 0)
			return "yes";
		else
			return "no";
	}

	public static String isNegative(int arg)
	{
		if (arg < 0)
			return "yes";
		else
			return "no";
	}

	public static String isFive(int arg)
	{
		if (arg == 5)
			return "yes";
		else
			return "no";
	}
}

